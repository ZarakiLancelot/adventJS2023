// Las luces son de dos colores: 游댮 y 游릭 . Para que el efecto sea el adecuado,
// siempre deben estar alternadas. Es decir, si la primera luz es roja,
// la segunda debe ser verde, la tercera roja, la cuarta verde, etc.

// Nos han pedido que escribamos una funci칩n adjustLights que, dado un array
// de strings con el color de cada luz (representados con los emojis 游댮
// para el rojo y 游릭 para el verde), devuelva el n칰mero m칤nimo de luces que hay
// que cambiar para que est칠n los colores alternos.

function adjustLights(lights){
  function checkColor(lights){
    let changes = 0;
    
    for (let i = 1; i < lights.length; i++){
      if (lights[i] === lights[i - 1]) {
        changes++;
        lights[i] = lights[i] === '游릭' ? '游댮' : '游릭';
      }    
    }
    
    return changes;
  }
  return Math.min(checkColor([...lights]), checkColor([...lights].reverse()));
}

console.log(adjustLights(['游릭', '游댮', '游릭', '游릭', '游릭']));
// -> 1 (cambias la cuarta luz a 游댮)

console.log(adjustLights(['游댮', '游댮', '游릭', '游댮', '游릭']));
// -> 1 (cambia la primera luz a verde)

console.log(adjustLights(['游댮', '游댮', '游릭', '游릭', '游댮']));
// -> 2 (cambias la segunda luz a 游릭 y la tercera a 游댮)

